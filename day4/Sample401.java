//for文
package day4;

public class Sample401 {
   public static void main(String[] args) {
      for(int i = 1;i <= 5;i++){  //iが5になるまで繰り返す
        System.out.print(i+" ");
      }
      System.out.println();
   }
}

//int i = 1　一番最初に実行されて二度と実行されない
//i <= 5　ループを繰り返す条件
//i++ インクリメントiを１プラスする 2を入れた場合2ずつ増えて行く

